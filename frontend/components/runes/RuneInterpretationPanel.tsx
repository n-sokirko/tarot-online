'use client';

import { useState } from 'react';
import { motion, AnimatePresence } from 'framer-motion';
import { ApiError, interpretRuneCast } from '@/lib/api';
import { MarkdownProse } from '@/lib/markdown';
import type { Interpretation } from '@/lib/types';

interface Props {
  castId: number;
  locale: 'ru' | 'en';
  initial: Interpretation | null;
}

const labels = {
  ru: {
    cta: 'Прочитать руны',
    loading: 'Руны говорят...',
    error: 'Что-то пошло не так. Попробуй ещё раз.',
    payRequired: 'Нужна Premium-подписка или кредиты, чтобы продолжить.',
    pricing: 'Открыть тарифы',
    footnote: 'Интерпретация сгенерирована AI. Это совет, а не приговор.',
    retry: 'Попробовать снова',
  },
  en: {
    cta: 'Read the runes',
    loading: 'The runes are speaking...',
    error: 'Something went wrong. Try again.',
    payRequired: 'Premium subscription or credits are needed to continue.',
    pricing: 'Open pricing',
    footnote: 'Interpretation generated by AI. Counsel, not verdict.',
    retry: 'Try again',
  },
} as const;

export default function RuneInterpretationPanel({ castId, locale, initial }: Props) {
  const t = labels[locale];
  const [interpretation, setInterpretation] = useState<Interpretation | null>(initial);
  const [loading, setLoading] = useState(false);
  const [error, setError] = useState<'generic' | 'payment' | null>(null);

  const run = async () => {
    setLoading(true);
    setError(null);
    try {
      setInterpretation(await interpretRuneCast(castId));
    } catch (e) {
      if (e instanceof ApiError && e.status === 402) setError('payment');
      else setError('generic');
    } finally {
      setLoading(false);
    }
  };

  if (interpretation) {
    return (
      <motion.section
        initial={{ opacity: 0, y: 16 }}
        animate={{ opacity: 1, y: 0 }}
        transition={{ duration: 0.6 }}
        className="w-full max-w-2xl mx-auto mt-12"
      >
        <div className="flex items-center gap-4 mb-6" aria-hidden>
          <div className="flex-1 h-px" style={{ background: 'rgba(212,175,55,0.2)' }} />
          <span style={{ color: 'rgba(212,175,55,0.6)', fontSize: '0.75rem', letterSpacing: '0.3em' }}>
            ᛟ
          </span>
          <div className="flex-1 h-px" style={{ background: 'rgba(212,175,55,0.2)' }} />
        </div>
        <MarkdownProse
          body={interpretation.body_md}
          className="font-serif text-[15px] md:text-base"
        />
        <p
          className="mt-8 text-[10px] tracking-widest uppercase opacity-50"
          style={{ color: 'rgba(201,194,224,0.5)', letterSpacing: '0.18em' }}
        >
          {t.footnote}
        </p>
      </motion.section>
    );
  }

  return (
    <section className="w-full max-w-2xl mx-auto mt-12 flex flex-col items-center">
      <AnimatePresence mode="wait">
        {loading ? (
          <motion.p
            key="loading"
            initial={{ opacity: 0 }}
            animate={{ opacity: [0.4, 1, 0.4] }}
            transition={{ duration: 1.5, repeat: Infinity, ease: 'easeInOut' }}
            className="font-serif italic"
            style={{ color: 'rgba(212,175,55,0.75)' }}
          >
            {t.loading}
          </motion.p>
        ) : error === 'payment' ? (
          <div className="flex flex-col items-center gap-3">
            <p className="font-serif text-sm" style={{ color: 'rgba(201,194,224,0.75)' }}>
              {t.payRequired}
            </p>
            <a
              href="/pricing"
              className="px-6 py-2.5 rounded-full text-xs tracking-widest uppercase"
              style={{
                background: 'linear-gradient(135deg, rgba(212,175,55,0.2), rgba(212,175,55,0.05))',
                border: '1px solid #d4af37',
                color: '#d4af37',
                letterSpacing: '0.18em',
              }}
            >
              {t.pricing}
            </a>
          </div>
        ) : error === 'generic' ? (
          <div className="flex flex-col items-center gap-3">
            <p className="font-serif text-sm" style={{ color: 'rgba(201,194,224,0.6)' }}>
              {t.error}
            </p>
            <button
              onClick={run}
              className="px-6 py-2 rounded-full text-xs tracking-widest uppercase"
              style={{
                border: '1px solid rgba(212,175,55,0.4)',
                color: 'rgba(212,175,55,0.85)',
                letterSpacing: '0.12em',
              }}
            >
              {t.retry}
            </button>
          </div>
        ) : (
          <motion.button
            key="cta"
            initial={{ opacity: 0 }}
            animate={{ opacity: 1 }}
            whileHover={{ scale: 1.03 }}
            whileTap={{ scale: 0.97 }}
            onClick={run}
            className="px-8 py-3 rounded-full text-xs tracking-widest uppercase"
            style={{
              background: 'linear-gradient(135deg, rgba(212,175,55,0.25), rgba(212,175,55,0.05))',
              border: '1px solid #d4af37',
              color: '#d4af37',
              letterSpacing: '0.22em',
              boxShadow: '0 0 24px rgba(212,175,55,0.15)',
            }}
          >
            ᛉ {t.cta}
          </motion.button>
        )}
      </AnimatePresence>
    </section>
  );
}
